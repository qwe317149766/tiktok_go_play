// MakeHex26_2 生成hex26_2签名
func MakeHex26_2(p14, p13 string) string {
	// 将p14转换为sm1数组 (小端序)
	sm1 := make([]uint32, 8)
	for i := 0; i < 8; i++ {
		bytes, _ := hex.DecodeString(p14[i*8 : i*8+8])
		sm1[i] = binary.LittleEndian.Uint32(bytes)
	}

	// 将p13转换为sm2数组 (小端序)
	sm2 := make([]uint32, 8)
	for i := 0; i < 8; i++ {
		bytes, _ := hex.DecodeString(p13[i*8 : i*8+8])
		sm2[i] = binary.LittleEndian.Uint32(bytes)
	}

	res := makeHalf(sm1) + makeHalf(sm2)
	return res
}
