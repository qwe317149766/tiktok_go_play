// 32位辅助函数
func add32(a, b uint32) uint32 {
	return (a + b) & 0xffffffff
}

func sub32(a, b uint32) uint32 {
	return (a - b) & 0xffffffff
}

func mul32(a, b uint32) uint32 {
	return uint32((uint64(a) * uint64(b)) & 0xffffffff)
}

func lsl32(v uint32, n int) uint32 {
	return (v << uint(n)) & 0xffffffff
}

func lsr32(v uint32, n int) uint32 {
	return v >> uint(n)
}

func ror32(v uint32, n uint32) uint32 {
	n &= 0x1f
	return ((v >> n) | (v << (32 - n))) & 0xffffffff
}

func bytesXor(rc1 byte, rc2 uint32) byte {
	return rc1 ^ byte(rc2&0xff) ^ byte((rc2>>8)&0xff) ^ byte((rc2>>16)&0xff) ^ byte((rc2>>24)&0xff)
}

func bytesXorNoRC1(rc2 uint32) byte {
	return byte(rc2&0xff) ^ byte((rc2>>8)&0xff) ^ byte((rc2>>16)&0xff) ^ byte((rc2>>24)&0xff)
}

